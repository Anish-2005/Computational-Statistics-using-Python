# Program to calculate the sum of two numbers

# Taking input from the user
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Calculating the sum
result = num1 + num2

# Printing the result
print("The sum of the two numbers is:", result)
